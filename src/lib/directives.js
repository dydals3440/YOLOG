export const customCallout = () => {
  return (tree) => {
    visit(tree, (node) => {
      if (node.type === "containerDirective") {
        if (!node.data) node.data = {};
        const data = node.data;
        const title = node.attributes?.title;
        const type = node.name;

        data.hName = "div";
        data.hProperties = {
          class: `callout ${type}`,
          "data-variant": title ? "title" : "inline",
        };

        const iconNode = {
          type: "html",
          value: getIconForType(type),
        };

        if (title) {
          const titleNode = {
            type: "element",
            children: [
              iconNode,
              {
                type: "element",
                children: [{ type: "text", value: title }],
              },
            ],
          };

          const contentNode = {
            type: "element",
            children: node.children,
          };

          node.children = [titleNode, contentNode];
        } else {
          if (node.children?.length > 0) {
            node.children.unshift(iconNode);
          }
        }
      }
    });
  };
};
